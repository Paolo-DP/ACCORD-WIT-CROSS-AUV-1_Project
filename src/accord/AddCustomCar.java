    private ArrayList<Integer> routeList = new ArrayList<>();
    private void btn_addRouteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addRouteActionPerformed
        int routeTurn = maneuverStringToInt(MANEUVER_STRINGS[cmb_route.getSelectedIndex()]);
        routeList.add(routeTurn);
    }//GEN-LAST:event_btn_addRouteActionPerformed

    private void btn_clearRouteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_clearRouteActionPerformed
        routeList.clear();
    }//GEN-LAST:event_btn_clearRouteActionPerformed

    private void btn_refreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_refreshActionPerformed
        if(!allInputsFilled()){
            txta_carDetails.setText("Please fill in all the details...");
            return;
        }
        int carID = Integer.parseInt(txtf_carID.getText(), 16);
        Car car = new SimulatedCar(carID);

        int x = Integer.parseInt(txtf_xLoc.getText());
        int y = Integer.parseInt(txtf_yLoc.getText());
        int throttle = Integer.parseInt(txtf_throttle.getText());
        double orient = Double.parseDouble(txtf_orient.getText());

        car.setAttributesManual(carID, x, y, orient, Car.DEFAULT_XDim, Car.DEFAULT_YDim, 0);
        car.adjustThrottle(throttle);
        for(int routeTurn : routeList){
            car.addRouteDirection(routeTurn);
        }
        newCustomCar = car;
        
        if(newCustomCar == null)
            return;
        
        String deets = "";
        deets += "Car ID: 0x" + Integer.toHexString(newCustomCar.getID());
        deets += "\nX: " + newCustomCar.getXLocation();
        deets += "\nY: " + newCustomCar.getXLocation();
        deets += "\nOrientation: " + newCustomCar.getOrientation();
        deets += "\nThrottle: " + newCustomCar.getThrottlePower();
        
        deets += "\nRoute List:";
        for(int route : routeList)
            deets += "\n" + route;
        
        txta_carDetails.setText(deets);
    }//GEN-LAST:event_btn_refreshActionPerformed

