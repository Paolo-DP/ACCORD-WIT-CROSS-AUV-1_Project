    private final String[] HEADING_STRINGS = {"EAST", "NORTH", "WEST", "SOUTH"};
    private final String[] MANEUVER_STRINGS = {"Left turn", "Straight", "Right turn"};
    private Car newCustomCar = null;
    boolean done = false;
    private void btn_doneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_doneActionPerformed
        if(!allInputsFilled()){
            txta_carDetails.setText("Please fill in all the details...");
            return;
        }
        int carID = Integer.parseInt(txtf_carID.getText(), 16);
        Car car = new SimulatedCar(carID);

        int x = Integer.parseInt(txtf_xLoc.getText());
        int y = Integer.parseInt(txtf_yLoc.getText());
        int throttle = Integer.parseInt(txtf_throttle.getText());
        double orient = Double.parseDouble(txtf_orient.getText());

        car.setAttributesManual(carID, x, y, orient, Car.DEFAULT_XDim, Car.DEFAULT_YDim, 0);
        car.adjustThrottle(throttle);
        for(int routeTurn : routeList){
            car.addRouteDirection(routeTurn);
        }
        newCustomCar = car;
        done = true;
        this.setVisible(false);
        accord_ui.updateCustomCarList(this);
    }//GEN-LAST:event_btn_doneActionPerformed
    private boolean allInputsFilled(){
        return !(txtf_carID.getText().isEmpty()
                || txtf_xLoc.getText().isEmpty()
                || txtf_yLoc.getText().isEmpty()
                || txtf_orient.getText().isEmpty()
                || txtf_throttle.getText().isEmpty());
    }
    public Car getNewCustomCar(){
        return newCustomCar;
    }
    
    private int headingStringToInt(String heading){
        switch(heading){
            case "EAST":
                return EAST;
            case "NORTH":
                return NORTH;
            case "WEST":
                return WEST;
            case "SOUTH":
                return SOUTH;
            default:
                return 0;
        }
    }
    private int maneuverStringToInt(String maneuver){
        switch(maneuver){
            case "Left turn":
                return LEFT_TURN;
            case "Straight":
                return STRAIGHT;
            case "Right turn":
                return RIGHT_TURN;
            default:
                return 0;
        }
    }
    
